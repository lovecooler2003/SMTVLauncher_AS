package com.shenma.tvlauncher.tvback.domain;
/**
 * @Description 电视日期Info
 * @author joychang
 *
 */
public class ChannelDateInfo {
	
	private String channelDate_Url;
	private String channelDate;
	private String channelDate_label;
	public String getChannelDate_Url() {
		return channelDate_Url;
	}
	public void setChannelDate_Url(String channelDate_Url) {
		this.channelDate_Url = channelDate_Url;
	}
	public String getChannelDate() {
		return channelDate;
	}
	public void setChannelDate(String channelDate) {
		this.channelDate = channelDate;
	}
	public String getChannelDate_label() {
		return channelDate_label;
	}
	public void setChannelDate_label(String channelDate_label) {
		this.channelDate_label = channelDate_label;
	}
	
	
}
