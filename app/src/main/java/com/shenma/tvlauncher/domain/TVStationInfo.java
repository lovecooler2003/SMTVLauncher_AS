package com.shenma.tvlauncher.domain;

public class TVStationInfo {
	
	private String id;				//id
	private String channelpic;		//tv台标路径
	private String channelid;		//tv台标id
	private String channelname;		//tv台标缩写
	private String channelstatus;	//状态
	private String channellink;		//连接
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getChannelpic() {
		return channelpic;
	}
	public void setChannelpic(String channelpic) {
		this.channelpic = channelpic;
	}
	public String getChannelid() {
		return channelid;
	}
	public void setChannelid(String channelid) {
		this.channelid = channelid;
	}
	public String getChannelname() {
		return channelname;
	}
	public void setChannelname(String channelname) {
		this.channelname = channelname;
	}
	public String getChannelstatus() {
		return channelstatus;
	}
	public void setChannelstatus(String channelstatus) {
		this.channelstatus = channelstatus;
	}
	public String getChannellink() {
		return channellink;
	}
	public void setChannellink(String channellink) {
		this.channellink = channellink;
	}
	
	
	
}
